
public class Product {
	private String name;
	private int stok;
	private String gorselUrl;
	private int indirimOranı;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getStok() {
		return stok;
	}

	public void setStok(int stok) {
		this.stok = stok;
	}

	public String getGorselUrl() {
		return gorselUrl;
	}

	public void setGorselUrl(String gorselUrl) {
		this.gorselUrl = gorselUrl;
	}

	public int getIndirimOranı() {
		return indirimOranı;
	}

	public void setIndirimOranı(int indirimOranı) {
		this.indirimOranı = indirimOranı;
	}

}
